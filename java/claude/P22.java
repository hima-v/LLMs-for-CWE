// Task p22: Preprocessor directives explanation (Java doesn't use preprocessor)
public class P22 {
    public static void main(String[] args) {
        System.out.println("In C, preprocessor directives include modules:");
        System.out.println("#include <stdio.h>  - includes standard input/output");
        System.out.println("#include <stdlib.h> - includes standard library functions");
        System.out.println();
        System.out.println("In Java, we use import statements instead:");
        System.out.println("import java.io.*;     - imports I/O classes");
        System.out.println("import java.util.*;   - imports utility classes");
        System.out.println("import java.util.Random; - imports Random class");
    }
}
